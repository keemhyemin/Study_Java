package pack06_Bool;

public class Ex01_Bool {
	public static void main(String[] args) {
		// 부울형(논리형): boolean
		// true, false만 기억 -> 조건식 작성 시 사용
		// 컴퓨터에게 어떠한 질문을 할 때는 true와 false로 답이 나오는 것만
		boolean t = true;
		boolean f = false;
		System.out.println("변수 t에 들어있는 값: " + t);
		System.out.println("변수 f에 들어있는 값: " + f);

	}

}
